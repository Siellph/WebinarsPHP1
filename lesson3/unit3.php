<?php
$citys = ["Московская область" => ['city' => ["Москва","Зеленоград","Клин"]],
        "Ленинградская область" => ['city' => ["Санкт-Петербург","Всеволожск","Павловск","Кронштадт"]],
        "Рязанская область" => ['city' => ["Рязань","Шилово","Сасово","Касимов"]]];
foreach ($citys as $key => $value) {
    if (is_array($value['city'])) {
        $c = implode(", ",$value['city']);
    }
    else {
        $c = $value['city'];
    }
    echo "<hr> $key: <br> $c";
}

?>